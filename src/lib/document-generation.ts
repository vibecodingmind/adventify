  // Closing
  y += 4;
  doc.text('Warm regards,', 20, y);

  if (params.pastorName) {
    y += 8;
    doc.setFont('helvetica', 'bold');
    doc.text(params.pastorName, 20, y);
    y += 5;
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(10);
    doc.text('Church Pastor', 20, y);
    y += 5;
    doc.text(params.churchName, 20, y);
  }

  // Clerk notes
  if (params.clerkNotes) {
    doc.setFontSize(8);
    doc.setFont('helvetica', 'italic');
    doc.setTextColor(100, 100, 100);
    const notesWrapped = doc.splitTextToSize(
      `Clerk Notes: ${params.clerkNotes}`,
      170
    );
    doc.text(notesWrapped, 20, y + 12);
    doc.setTextColor(0, 0, 0);
  }

  // Doc ID & dates
  drawDocIdAndDates(doc, params.requestId, generatedAt, expiryDate);

  // Footer
  drawFooter(doc);

  return doc;
